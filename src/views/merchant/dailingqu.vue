<template>
	<div class="main-content">
	  	<div class="left-container">
	    	<el-menu default-active="2" class="" mode="horizontal" router style="margin-bottom: 20px;">
		      	<el-menu-item index="1" :route="{path:'/merchant/index'}">已入驻商家</el-menu-item>
            <el-menu-item index="3" :route="{path:'/merchant/paid'}">付费商家</el-menu-item>
            <el-menu-item index="4" :route="{path:'/merchant/recommend'}">推荐商家</el-menu-item>
            <el-menu-item index="2" :route="{path:'/merchant/dailingqu'}">待领取商家</el-menu-item>
	    	</el-menu>
        <el-row type="flex" class="filter-container" style="margin-bottom: 20px;">
          <el-button type="primary" size="small" style="min-width: 120px; margin-right: 20px;" icon="el-icon-circle-plus-outline" @click="goCreate">添加商家</el-button>
        </el-row>
        <el-row class="list">
          <el-table
            v-loading="listLoading"
            :data="list"
            border
            fit
            highlight-current-row
            style="width: 100%"
            size="small"
            :header-cell-style="{
              'background-color': '#f7f9fa',
              'color': '#637282;'
            }"
            >
            <el-table-column
              prop="id"
              label="ID"
              width="60">
              <template slot-scope="{row}">
                <span>{{ row.id }}</span>
              </template>
            </el-table-column>
            <el-table-column
              label="LOGO"
              width="60">
              <template slot-scope="{row}">
                <img :src="row.logo" style="width: 40px;height: 40px;">
              </template>
            </el-table-column>
            <el-table-column
              width="180px"
              label="商家名称">
              <template slot-scope="{row}">
                <span>{{ row.name }}</span>
              </template>
            </el-table-column>
            <el-table-column
              label="活动总数"
              width="70px">
              <template slot-scope="{row}">
                <span>{{ row.activities ? row.activities.length : '0' }}</span>
              </template>
            </el-table-column>
            <el-table-column
              label="访问人数"
              width="70px">
              <template slot-scope="{row}">
                <span>-</span>
              </template>
            </el-table-column>
            <el-table-column
              label="参与人数"
              width="70px">
              <template slot-scope="{row}">
                <span>-</span>
              </template>
            </el-table-column>
            <el-table-column
              label="累计收入"
              width="70px">
              <template slot-scope="{row}">
                <span>-</span>
              </template>
            </el-table-column>
            <el-table-column
              label="门店数量"
              width="70px">
              <template slot-scope="{row}">
                <span>-</span>
              </template>
            </el-table-column>
            <el-table-column
              label="员工数量"
              width="70px">
              <template slot-scope="{row}">
                <span>-</span>
              </template>
            </el-table-column>
            <el-table-column
              label="会员"
              width="">
              <template slot-scope="{row}">
                <span>{{ levels[row.vipLevel] }}</span>
              </template>
            </el-table-column>
            <el-table-column
              label="会员到期时间">
              <template slot-scope="{row}">{{ row.vipEndTime | moment("YYYY-MM-DD HH:mm:ss") }}
              </template>
            </el-table-column>
            <el-table-column
              label="状态"
              width="70px">
              <template slot-scope="{row}">
                <el-tag type="success" size="mini" v-if="!row.deletedAt">上架</el-tag>
                <el-tag type="info" size="mini" v-else>已下架</el-tag>
              </template>
            </el-table-column>
            <el-table-column label="操作" width="120px">
              <template slot-scope="scope">
                <el-button
                  size="mini"
                  type="text"
                  @click="handleView(scope.$index, scope.row)">查看</el-button>
                <el-button
                  size="mini"
                  type="text"
                  @click="handleEdit(scope.$index, scope.row)">编辑</el-button>
                <el-button
                  size="mini"
                  type="text"
                  v-if="scope.row.deletedAt"
                  @click="handleUnderOrGrounding(scope.$index, scope.row, 1)">上架</el-button>
                <el-button
                  size="mini"
                  type="text"
                  v-else
                  style="color: #F56C6C"
                  @click="handleUnderOrGrounding(scope.$index, scope.row, 0)">下架</el-button>
              </template>
            </el-table-column>
          </el-table>
          <pagination v-show="total>0" :total="total" :page.sync="listQuery.current" :limit.sync="listQuery.size" @pagination="getList" />
        </el-row>
      </div>
      <!--<div class="secondary-sidebar"></div>-->
  </div>
</template>

<script>
import { fetchList, groundingOrUndercarriage } from '@/api/merchant'
import Pagination from '@/components/Pagination' // secondary package based on el-pagination

export default {
  components: { Pagination },
  data() {
    return {
      list: null,
      total: 0,
      listLoading: false,
      listQuery: {
        searchStr: '',
        current: 1,
        size: 20
      },
      listFilter: {
        authStatus: 5 //待领取商家
      },
      levels: { 0: '标准会员', 1: '体验会员', 2: 'VIP会员' }, // '会员级别 0普通会员 1 体验会员 2会员'
      typeOptions: [{ key: 2, label: 'VIP会员' }] // '会员级别 0普通会员 1 体验会员 2会员'
    };
  },
  created() {
    this.getList()
  },
  methods: {
    getList() {
      this.listLoading = true
      fetchList(this.listQuery, this.listFilter).then(response => {
        this.list = response.data.records
        this.total = response.data.total
        this.listLoading = false
      })
    },
    handleFilter() {
      this.listQuery.current = 1
      this.getList()
    },
    sortChange(data) {
      const { prop, order } = data
      if (prop === 'id') {
        this.sortByID(order)
      }
    },
    sortByID(order) {
      if (order === 'ascending') {
        this.listQuery.sort = '+id'
      } else {
        this.listQuery.sort = '-id'
      }
      this.handleFilter()
    },
    // 添加商家
    goCreate() {
      this.$router.push({ path: '/merchant/edit/0' })
    },
    // 编辑商家
    handleEdit(index, row) {
      this.$router.push({ path: '/merchant/edit/' + row.id })
    },
    handleView(index, row) {
      this.$router.push({
        path: '/merchant/detail/' + row.id
      })
    },
    handleUnderOrGrounding(index, row, type) {
      this.$confirm('确认' + (type ? '上架' : '下架') +'商家?', '提示', {
        confirmButtonText: '确定',
        cancelButtonText: '取消',
        type: 'warning'
      }).then(() => {
        groundingOrUndercarriage({merchantId: row.id, type: type}).then(response => {
          if (response.code === '200') {
            this.$message({
              type: 'success',
              message: '操作成功!'
            })
            this.getList()
          } else {
            this.$message({
              type: 'error',
              message: response.msg
            })
          }
        }) 
      }).catch(() => {
        this.$message({
          type: 'info',
          message: '已取消操作'
        })        
      })
    }
  }
}
</script>

